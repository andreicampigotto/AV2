package ifc.edu.br.av2.model;

public class Marina {
    
    private int totalVagas;

    public Marina(int totalVagas) {
        this.totalVagas = totalVagas;
    }

    public Marina() {
    }

    public int getTotalVagas() {
        return totalVagas;
    }

    public void setTotalVagas(int totalVagas) {
        this.totalVagas = totalVagas;
    }
    
}
